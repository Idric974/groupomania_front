<template>
  <div class="create-post">
    <div class="btn_acc2"><BtnLogout /></div>
    <div class="btn_acc2"><BtnHome /></div>

    <br />

    <hr />

    <div class="menu_composant">
      <h1>Modifier mon commentaire</h1>
    </div>

    <div class="post-form">
      <form class="form-setup">
        <input
          type="text"
          name="title"
          validation="required"
          class="title"
          v-model="input.title"
        />

        <input
          type="textarea"
          name="content"
          validation="required"
          class="content"
          v-model="input.content"
        />

        <button
          type="submit"
          v-on:click.prevent="updateComment()"
          class="poster"
        >
          Modifier Commentaire
        </button>
      </form>
    </div>
  </div>
</template>

//*✂️➖➖✂️➖➖✂️➖➖✂️➖➖✂️➖➖✂️➖➖✂️➖➖✂️➖➖✂️➖

<script>
import BtnLogout from "@/components/BtnLogout.vue";
import BtnHome from "@/components/BtnHome.vue";
export default {
  components: { BtnHome, BtnLogout },
  name: "CreatePost",
  data() {
    return {
      input: {
        title: "",
        content: "",
      },
    };
  },
  methods: {
    readComment() {
      console.log("Hello");
      const params = this.$route.params.id;
      console.log(params);

      const userIdStorage = localStorage.getItem("groupomania");
      const objJson = JSON.parse(userIdStorage);
      const token = objJson.token;

      //* ✅ 👉 Définition des en-têtes.
      const headers = new Headers();
      headers.append("Authorization", `Bearer ${token}`);
      headers.append("Content-Type", "application/json");

      //* ✅ 👉 Définition de l'URL de la requête.
      let url = "http://localhost:3000/api/comment/findOneComment/" + params;
      console.log(url);

      //* ✅ 👉 Définition des paramètres de la requête.
      const parametresDeRequete = {
        method: "GET",
        headers: headers,
      };

      fetch(url, parametresDeRequete)
        .then((success) => {
          success.json().then((result) => {
            this.comments = result.comments;
            this.input.title = this.comments.title;
            this.input.content = this.comments.comment;
          });
        })
        .catch(function(error) {
          console.log(error);
        });
    },

    //*➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖

    //* ✅ 👉 Mise à jour du profil enregistrées.
    updateComment() {
      const storage = localStorage.getItem("groupomania");
      const objJson = JSON.parse(storage);
      const token = objJson.token;
      const params = this.$route.params.id;

      const values = {
        title: this.input.title,
        content: this.input.content,
      };

      //* ✅ 👉 Définition du body de la requête.
      const body = JSON.stringify(values);
      console.log("✔️✔️✔️ 👉  BODY", body);

      //* ✅ 👉 Définition des en-têtes.
      const headers = new Headers();
      headers.append("Authorization", `Bearer ${token}`);
      headers.append("Content-Type", "application/json; charset=UTF-8");

      console.log("✔️✔️✔️ 👉  HEADERS", headers);

      //* ✅ 👉 Définition de l'URL de la requête.
      let url = "http://localhost:3000/api/comment/updateComment/" + params;
      console.log("✔️✔️✔️ 👉  URL", url);

      //* ✅ 👉 Définition des paramètres de la requête.
      const parametresDeRequete = {
        method: "POST",
        headers: headers,
        body: body,
      };
      console.log("✔️✔️✔️ 👉 PARAMÈTRES DE REQUÊTE", parametresDeRequete);

      fetch(url, parametresDeRequete)
        .then(function(response) {
          if (response.status !== 200) {
            console.log(
              "Looks like there was a problem. Status Code: " + response.status
            );
            return;
          }

          response.json().then(function(data) {
            console.log(data);
            window.location.reload();
            alert("⚠️ Votre commentaire a été modifié ⚠️ ");
            window.history.go(-1);
          });
        })
        .catch(function(err) {
          console.log("❌❌❌ CATCH a Fetch Error :-S", err);
        });
    },
    //*➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖➖
  },
  mounted() {
    this.readComment();
  },
};
</script>

